    private ServerSocket server;
    private final int PUERTOH=1000; //variable por la que sale el puerto
    
    

    public frm_Servidor() {
        initComponents();
        
        try {
            
            server = new ServerSocket(PUERTOH);//puerto para la conexión
            mensajeria("*.:Servidor con conexión:.* \n");
            
            super.setVisible(true);//nos permite ver el servidor
            
            while(true){
            
                Socket cliente=server.accept();
                mensajeria("Cliente conectado desde la dirección: "+cliente.getInetAddress().getHostAddress());//muestra la ip
                
                DataInputStream entrada = new DataInputStream(cliente.getInputStream());
                
                HiloServidor hilo = new HiloServidor(cliente,entrada.readUTF(),this);
                hilo.start();
                
            
            
            }
            
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString());
        }
    }
    
    
