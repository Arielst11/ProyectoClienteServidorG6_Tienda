    public void limpiarCarritoYlabels(){
Objeto.arrayListCarritoDeCompras.clear();
DefaultTableModel contenidoTabla = (DefaultTableModel) jTArticulos.getModel();
for(int i = contenidoTabla.getRowCount() ; i != 0 ; i--){
 contenidoTabla.removeRow(0);   
}
cargarArticulosCarrito();
jLprecioDeObjetos.setText("0");
    }
    
    
    
  public float precioDeTodosLosItems = 0;
   
    public void cargarArticulosCarrito(){
     ArrayList<Objeto> arrayListaArticulos = new ArrayList<Objeto>();  
     arrayListaArticulos = Objeto.arrayListCarritoDeCompras;
     DefaultTableModel contenidoTabla = (DefaultTableModel) jTArticulos.getModel();
     
     Object rowDeTabla[] = new Object [5];
         
           if (arrayListaArticulos!=null){
            
            for (int i=0; i<arrayListaArticulos.size();i++){
                
                rowDeTabla[0]= arrayListaArticulos.get(i).getCodigoArticulo();
                rowDeTabla[1]= arrayListaArticulos.get(i).getPrecioArticulo();
                rowDeTabla[2]= arrayListaArticulos.get(i).getNombreArticulo();
                rowDeTabla[3]= arrayListaArticulos.get(i).getDescripcionArticulo();
                rowDeTabla[4]= 1;
                contenidoTabla.addRow(rowDeTabla);
                
                precioDeTodosLosItems = precioDeTodosLosItems +  arrayListaArticulos.get(i).getPrecioArticulo();
                
            }
            jTArticulos.setModel(contenidoTabla);
            
        }else{
            JOptionPane.showMessageDialog(null,"No existen artículos en el carrito de compras");
        }
         
        
    }
    
